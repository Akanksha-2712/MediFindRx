-- 1. EXTENSIONS
create extension if not exists "uuid-ossp";

-- 2. CREATE BASE TABLES (If Not Exists)
create table if not exists profiles (
  id uuid references auth.users on delete cascade not null primary key,
  email text,
  name text,
  role text check (role in ('customer', 'pharmacy', 'admin', 'hospital')),
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table if not exists drugs (
  id bigint generated by default as identity primary key,
  name text not null,
  dosage text,
  type text,
  price numeric,
  description text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table if not exists pharmacies (
  id bigint generated by default as identity primary key,
  name text not null,
  address text,
  phone text,
  rating numeric default 0,
  owner_id uuid references auth.users,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  approved boolean default false,  -- Ensure column exists
  latitude numeric,                -- Ensure column exists
  longitude numeric                -- Ensure column exists
);

create table if not exists hospitals (
  id uuid references auth.users on delete cascade not null primary key,
  name text not null,
  address text,
  phone text,
  beds_total integer default 20,
  beds_occupied integer default 0,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  approved boolean default false,  -- Ensure column exists
  latitude numeric,                -- Ensure column exists
  longitude numeric                -- Ensure column exists
);

create table if not exists inventory (
  id bigint generated by default as identity primary key,
  pharmacy_id bigint references pharmacies(id) on delete cascade,
  drug_id bigint references drugs(id) on delete cascade,
  stock integer default 0,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique(pharmacy_id, drug_id)
);

create table if not exists reservations (
  id bigint generated by default as identity primary key,
  pharmacy_id bigint references pharmacies(id),
  drug_id bigint references drugs(id),
  user_id uuid references auth.users,
  customer_name text,
  otp text,
  status text default 'pending',
  quantity integer default 1,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table if not exists hospital_appointments (
  id bigint generated by default as identity primary key,
  hospital_id uuid references hospitals(id) on delete cascade,
  patient_name text not null,
  scheduled_time timestamp with time zone not null,
  status text default 'scheduled',
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table if not exists emergency_requests (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users,
  customer_name text,
  latitude numeric,
  longitude numeric,
  status text default 'pending',
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table if not exists medicine_requests (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users not null,
  customer_name text,
  drug_name text not null,
  latitude numeric not null,
  longitude numeric not null,
  status text default 'pending',
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table if not exists pharmacy_responses (
  id bigint generated by default as identity primary key,
  request_id bigint references medicine_requests(id) on delete cascade not null,
  pharmacy_id bigint references pharmacies(id),
  pharmacy_name text,
  status text default 'available',
  price numeric,
  message text,
  pharmacy_lat numeric,
  pharmacy_lng numeric,
  drug_id bigint references drugs(id),
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

